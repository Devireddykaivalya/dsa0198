#include <iostream>
using namespace std;

int main() {
    int a = 10;
    int b = 5;

    cout << "sum = " << (a + b) << endl;
    cout << "sub = " << (a - b) << endl;
    cout << "prod = " << (a * b) << endl;
    cout << "div = " << (a / b) << endl;
    cout << "mod = " << (a % b) << endl;

    return 0;
}

